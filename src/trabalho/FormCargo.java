    public void atualizaTabelaCargo(){
        Cargo cargo = new Cargo();
        
        try {
            listaCargo = cargo.listarCargos();
            
            jt_cargo = createJtableCargo( listaCargo );
            jScrollPane9.setViewportView(jt_cargo);
            
        } catch (SQLException ex) {
            Logger.getLogger(CadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public JTable createJtableCargo( ArrayList<Cargo> listaCargo ){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[] 
        { "ID", "Cargo","Departamento"};
         
         for (Cargo d:listaCargo){
            dados.add(new String[]
                { d.getIdCargo()+"", d.getCargo(), d.getDepartamentoString()}
            );
         }
         
         SimpleTableModel modelo =
                new SimpleTableModel(dados, colunas);
         
         JTable jtable = new JTable((TableModel) modelo);
         return jtable;
    }
    
