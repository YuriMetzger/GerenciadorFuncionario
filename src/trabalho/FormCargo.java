    private void cadastro_cargo_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastro_cargo_salvarActionPerformed
        Cargo cargo = new Cargo();
        Departamento departamento = new Departamento();

        if( !cadastro_cargo.getText().equalsIgnoreCase("") ){

            String cargotext          = cadastro_cargo.getText();
            String departamento_text  = (String)cadastro_cargo_departamento.getSelectedItem();
            int departamento_id       = 0;

            try {
                listaDepartamento = departamento.listarDepartamentos();

                for (int i = 0; i < listaDepartamento.size(); i++) {
                    if( listaDepartamento.get(i).getDepartamento().equalsIgnoreCase( departamento_text ) ){
                        departamento_id = listaDepartamento.get(i).getIdDepartamento();
                    }
                }

                cargo.setCargo(cargotext);
                cargo.setDepartamento(departamento_id);

                if(idCargoEditar == "0"){
                    cargo.cadastrarCargo();
                }else{
                    cargo.setIdCargo(Integer.parseInt(idCargoEditar));
                    cargo.EditarCargo();
                    idCargoEditar = "0";
                }
                cadastro_cargo.setText("");
                atualizaTabelaCargo();
            } catch (SQLException ex) {
                Logger.getLogger(CadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }

        }else{
            JOptionPane.showMessageDialog(null, "Digite um cargo!");
        }
    }//GEN-LAST:event_cadastro_cargo_salvarActionPerformed

