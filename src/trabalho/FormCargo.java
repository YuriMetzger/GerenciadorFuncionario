    private void cadastro_cargo_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastro_cargo_excluirActionPerformed
        Object[] options = {"   Sim   ", "   NÃ£o   "};
        int n = JOptionPane.showOptionDialog(null," Deseja realmente Excluir?","Sair do Programa",
            JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE,null,options,options[1]);
        if(n == 0){
            Integer idx = 0;
            String id_cargo = "0";

            Cargo cargo = new Cargo();

            idx = jt_cargo.getSelectedRow();
            if( idx == -1){
                JOptionPane.showMessageDialog(null, " Selecionar um item! ");
            }else{
                id_cargo =  (String) jt_cargo.getModel().getValueAt(idx, 0);

                cargo.setIdCargo( Integer.parseInt(id_cargo) );
                try {
                    cargo.deletarCargo();
                    atualizaTabelaCargo();
                } catch (SQLException ex) {
                    Logger.getLogger(CadFuncionario.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        }
    }//GEN-LAST:event_cadastro_cargo_excluirActionPerformed

